using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;

namespace ayudamigrante_netcore.Controllers
{
    public class UserController : Controller
    {
